
public class Reservierung {

	public int bnr;
	public int start;
	public int end;
	public int pers;
	public int knr;
	public int jahr;
	
	public Reservierung(int bnr, int start, int end, int pers, int knr, int jahr) {
		super();
		this.bnr = bnr;
		this.start = start;
		this.end = end;
		this.pers = pers;
		this.knr = knr;
		this.jahr = jahr;
	}
	
}
